    <script>
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="_token"]').attr('content')
                }
            });
            $('#exam_code').keyup(function(){
            var exam_code = $(this).val();
            var id_lenght= exam_code.toString().length;
            if(id_lenght>3){
            $.ajax({
            type: "POST",
            url: "{{route('check.exams')}}",
            data: {'exam_code':exam_code},
            dataType: 'json',
            success : function(data) {
            if(data.exam_code)
                 {
                     $('#presentreg').html('Opss.!..Exam Code Already Exist');
                     $('#absentreg').html('');
                 }else{

                    $('#absentreg').html('Congrats!..Exam Code is Available.');
                    $('#presentreg').html('');
                }
            },
            error: function(response) {
                alert(response.responseJSON.message);
                    }
                });
            }
        });
    </script>
